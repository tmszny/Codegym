/* 
Trójkąt z ósemek
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        int n = 1;
        for(int i = 10; i > 0; i--){
            for (int j = n; j >= 1; j--){
                System.out.print("8");
            }
            System.out.println();
            n++;

        }

    }
}
